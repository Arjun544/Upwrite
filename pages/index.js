import WriteProposal from "@/components/WriteProposal";
import Response from "@/components/Response";
import Head from "next/head";
import { createContext, useState } from "react";
import { Toaster } from "react-hot-toast";

export const AppContext = createContext(null);

export default function Home() {
  const [currentStep, setCurrentStep] = useState(1);
  const [proposal, setProposal] = useState({});
  const [proposalInput, setProposalInput] = useState("");
  const [about, setAbout] = useState("");
  const [questions, setQuestions] = useState([]);

  return (
    <AppContext.Provider
      value={{
        setCurrentStep,
        proposal,
        setProposal,
         proposalInput,
        setProposalInput,
        about,
        setAbout,
        questions,
        setQuestions,
      }}
    >
      <Head>
        <title>Upwrite</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      {/* <h1>Say goodbye to proposal struggles</h1> */}

      <div className="flex w-full min-h-screen gap-8 pr-4 pl-28 overflow-hidden">
        <Toaster containerClassName="text-sm"/>

        {currentStep === 1 ? (
          <WriteProposal setCurrentStep={setCurrentStep} />
        ) : (
          <Response />
        )}
      </div>
    </AppContext.Provider>
  );
}
